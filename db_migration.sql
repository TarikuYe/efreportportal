-- 0. Auto-populate day_of_week on daily_work_logs
CREATE OR REPLACE FUNCTION set_day_of_week()
RETURNS TRIGGER AS $$
BEGIN
  NEW.day_of_week := to_char(NEW.log_date, 'FMDay');
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_set_day_of_week ON daily_work_logs;
CREATE TRIGGER trg_set_day_of_week
  BEFORE INSERT OR UPDATE ON daily_work_logs
  FOR EACH ROW
  EXECUTE FUNCTION set_day_of_week();

-- 1. Performance Evaluation automated scoring and ranking
CREATE OR REPLACE FUNCTION calculate_performance_eval()
RETURNS TRIGGER AS $$
BEGIN
  -- Score formula: Tech*0.40 + Prod*0.30 + Punc*0.10 + Comm*0.05 + Rep*0.05 + Adapt*0.10
  NEW.total_score := (NEW.tech_competence_score * 0.40) +
                     (NEW.productivity_score * 0.30) +
                     (NEW.punctuality_score * 0.10) +
                     (NEW.communication_score * 0.05) +
                     (NEW.reporting_score * 0.05) +
                     (NEW.adaptability_score * 0.10);

  -- Determine tier
  IF NEW.total_score >= 90.00 THEN
    NEW.performance_level := 'Outstanding';
  ELSIF NEW.total_score >= 80.00 THEN
    NEW.performance_level := 'Very Good';
  ELSIF NEW.total_score >= 70.00 THEN
    NEW.performance_level := 'Good';
  ELSIF NEW.total_score >= 60.00 THEN
    NEW.performance_level := 'Satisfactory';
  ELSE
    NEW.performance_level := 'Needs Improvement';
  END IF;
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_calculate_performance_eval ON performance_evaluations;
CREATE TRIGGER trg_calculate_performance_eval
  BEFORE INSERT OR UPDATE ON performance_evaluations
  FOR EACH ROW
  EXECUTE FUNCTION calculate_performance_eval();

-- 2. Correspondence Register automated response due date and status
CREATE OR REPLACE FUNCTION handle_correspondence_status()
RETURNS TRIGGER AS $$
BEGIN
  -- Set response_due_date = date_logged + 7 days if response is required and not manually set
  IF NEW.response_required = TRUE THEN
    IF NEW.response_due_date IS NULL THEN
      NEW.response_due_date := NEW.date_logged + INTERVAL '7 days';
    END IF;
  ELSE
    NEW.response_due_date := NULL;
  END IF;

  -- Establish status
  IF NEW.response_required = FALSE THEN
    NEW.status := 'Not Required';
  ELSIF NEW.response_sent_date IS NOT NULL THEN
    NEW.status := 'Closed';
  ELSIF NEW.response_due_date < CURRENT_DATE THEN
    NEW.status := 'Overdue';
  ELSE
    NEW.status := 'Open';
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_handle_correspondence_status ON correspondence_register;
CREATE TRIGGER trg_handle_correspondence_status
  BEFORE INSERT OR UPDATE ON correspondence_register
  FOR EACH ROW
  EXECUTE FUNCTION handle_correspondence_status();

