-- Create custom enums if they do not exist
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'user_role') THEN
    CREATE TYPE user_role AS ENUM ('engineer', 'admin', 'dgm');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'approval_status') THEN
    CREATE TYPE approval_status AS ENUM ('Pending', 'Approved', 'Returned');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'direction_enum') THEN
    CREATE TYPE direction_enum AS ENUM ('Incoming', 'Outgoing');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'correspondence_category') THEN
    CREATE TYPE correspondence_category AS ENUM ('NOC', 'General', 'RFI', 'EOT Claim', 'Variation', 'Payment');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'bond_type_enum') THEN
    CREATE TYPE bond_type_enum AS ENUM ('Advance Payment Bond', 'Performance Bond');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'bond_status_enum') THEN
    CREATE TYPE bond_status_enum AS ENUM ('Active', 'Expired', 'Released');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'eot_status_enum') THEN
    CREATE TYPE eot_status_enum AS ENUM ('Approved', 'Rejected', 'Pending', 'Under Review');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'eot_status_alert_enum') THEN
    CREATE TYPE eot_status_alert_enum AS ENUM ('OK', 'Nearly Expired', 'Expired');
  END IF;
  
  IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'performance_level_enum') THEN
    CREATE TYPE performance_level_enum AS ENUM ('Outstanding', 'Very Good', 'Good', 'Satisfactory', 'Needs Improvement');
  END IF;
END
$$;

