--   2. RLS policies (INSERT + SELECT for engineers, deny UPDATE
--      and DELETE for all authenticated roles) add a
--      declarative second layer at the query planner level.
--   3. Approval workflow moved to daily_work_log_reviews
--      (append-only) so admins can record decisions without
--      touching the immutable source rows.
